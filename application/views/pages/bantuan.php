    <ol>
        <li>Pilih jenis data yang ditambahkan. (Sawah / Pemukiman)</li>
        <li>Pilih gambar peta blok yang bersesuaian. <strong>(File yang diunggah harus berisi entri yang memiliki gambar yang sama)</strong></li>
        <li>Pilih file Microsot Excel <strong>(Pastikan file memiliki ekstensi .xls / .xlsx CONTOH: "unggah.xls")</strong></li>
